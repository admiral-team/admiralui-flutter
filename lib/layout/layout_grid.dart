/// Default layout metric constants
class LayoutGrid {
  /// Base layout module
  static const double module = 8.0;

  /// Half layout module
  static const double halfModule = module / 2;

  /// Double layout module
  static const double doubleModule = module * 2.0;

  /// Triple layout module
  static const double tripleModule = module * 3.0;

  /// Quadruple layout module
  static const double quadrupleModule = module * 4.0;
}
