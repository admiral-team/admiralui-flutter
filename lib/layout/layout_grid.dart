/// Default layout metric constants
class LayoutGrid {
  /// Base layout module
  static double module = 8.0;

  /// Half layout module
  static double halfModule = module / 2;

  /// One and a half layout module
  static double oneAndHalfModule = module * 1.5;

  /// Double layout module
  static double doubleModule = module * 2.0;

  /// Triple layout module
  static double tripleModule = module * 3.0;

  /// Quadruple layout module
  static double quadrupleModule = module * 4.0;
}
